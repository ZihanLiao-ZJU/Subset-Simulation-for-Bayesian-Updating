function C = logmultiply (A,B)
% Logarithmic version of A*B
% Syntax:
% -----------------------------------------------------------------------------------
% C = logmultiply (A,B)
% -----------------------------------------------------------------------------------
%
% Inputs:
% -----------------------------------------------------------------------------------
% A    : logarithmic matrix
% B    : logarithmic matrix
% -----------------------------------------------------------------------------------
%
% Outputs:
% -----------------------------------------------------------------------------------
% C    : multiplicated (in logarithm) array
% -----------------------------------------------------------------------------------
m = size(A,1);
n = size(B,2);
C = zeros(m,n);
for irow = 1:m
    C(irow,:) = logsum(A(irow,:)'+B,1);
end
return